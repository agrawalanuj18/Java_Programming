public class p17 {
    public static void main(String[] args) {
        // Loops
        // 1-100
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }

        // 100-1
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }

        

    }
}
