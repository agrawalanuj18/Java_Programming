public class p18 {
    public static void main(String[] args) {
        // While Loop
        int i = 1;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }
        int b = 100;
        while (b >= 1) {
            System.out.println(b);
            b--;
        }
    }
}
