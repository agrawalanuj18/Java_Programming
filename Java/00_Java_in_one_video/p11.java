public class p11 {
    public static void main(String[] args){
        // Operators
        
        //Arithmetic Operator
        
        int a = 1;
        int b = 2;
        int diff = a - b;
        int mul = a * b;
        int add =  a + b ;
        int divide = a / b ; 
        System.out.println(add);
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(divide);
        
        // modulo

        int modulo = a % b;
        System.out.println(modulo);

        //Assignment Operator

        // Increment Function 
        int numb = 1;
        System.out.println(numb++);
        System.out.println(numb);
        System.out.println(++numb);
        System.out.println(numb);
        System.out.println(numb--);
        System.out.println(numb);
        System.out.println(--numb);
        System.out.println(numb);

    }
}
