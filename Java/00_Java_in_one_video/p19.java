public class p19 {
    public static void main(String[] args) {
        // Do-While Loop
        // 1-100
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 100);

        do {
            --i;
            System.out.println(i);

        } while (i >= 1);
    }
}
