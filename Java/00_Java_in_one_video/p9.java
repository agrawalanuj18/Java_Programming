public class p9 {
    public static void main(String[] args){
        //casting
        
        //Implicit Casting 

        double price = 100.00;
        double finalPrice = price + 18;

        System.out.println(finalPrice);

        // Explicit Casting

        int p = 100;
        int fp = p + (int)18.18;

        // decimal ke bhaad wale sab zero ho jaate h 
        //roundoff nhi hota h isshme
        

        System.out.println(fp);
    }
}
